/**
 * Подстановка дефолтных значений в ACF-поля при первом открытии (если пусто)
 */
function fabrica_legal_sections_to_acf_format($sections) {
    $out = array();
    foreach ($sections as $sec) {
        $items_acf = array();
        foreach ((array) (isset($sec['items']) ? $sec['items'] : array()) as $txt) {
            $items_acf[] = array('item' => $txt);
        }
        $out[] = array(
            'section_title'  => isset($sec['title']) ? $sec['title'] : '',
            'section_items'  => $items_acf,
        );
    }
    return $out;
}

