/**
 * Собрать документ из полей ACF (intro + repeater sections)
 *
 * @param string $intro_key   имя поля ввода (напр. legal_privacy_intro)
 * @param string $sections_key имя поля повторителя (напр. legal_privacy_sections)
 * @param string $default_title заголовок по умолчанию
 * @return array|null массив title, intro, sections или null если в ACF пусто
 */
function fabrica_get_legal_doc_from_acf($intro_key, $sections_key, $default_title = '') {
    if (!function_exists('get_field')) {
        return null;
    }
    $intro = get_field($intro_key, 'option');
    $sections_raw = get_field($sections_key, 'option');
    if (!is_array($sections_raw) || empty($sections_raw)) {
        return null;
    }
    $sections = array();
    foreach ($sections_raw as $row) {
        $items = array();
        if (!empty($row['section_items']) && is_array($row['section_items'])) {
            foreach ($row['section_items'] as $it) {
                if (isset($it['item']) && trim((string) $it['item']) !== '') {
                    $items[] = trim($it['item']);
                }
            }
        }
        $sections[] = array(
            'title'  => isset($row['section_title']) ? $row['section_title'] : '',
            'items'  => $items,
        );
    }
    return array(
        'title'    => $default_title,
        'intro'    => is_string($intro) ? $intro : '',
        'sections' => $sections,
    );
}

/**
 * Возвращает название компании для подстановки в документы
 */
function fabrica_legal_operator_name() {
    $company = fabrica_get_company_info();
    return isset($company['legal_name']) ? $company['legal_name'] : 'ИП';
}

